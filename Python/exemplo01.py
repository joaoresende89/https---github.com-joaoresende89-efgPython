# Operadores aritiméticos
# + - * / % ** //

valor1 = 50
valor2 = 13

print("Valor1:", valor1, "Valor2:", valor2)
print("Subtração", valor1 - valor2)
print("Soma", valor1 + valor2)
print("Multiplicação", valor1 * valor2)
print("Divisão", valor1 / valor2)
print("Resto", valor1 % valor2)
print("Potencia valor1^valor2", valor1 ** valor2)
print("Quociente", valor1 // valor2)

